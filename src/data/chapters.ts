function generatePages(chapterNum: number, pageCount: number, startParagraph: number): Page[] {
  const pages: Page[] = [];
  for (let i = 0; i < pageCount; i++) {
    const paragraphsPerPage = 3;
    const content: string[] = [];
    for (let j = 0; j < paragraphsPerPage; j++) {
      const idx = (startParagraph + i * paragraphsPerPage + j) % loremParagraphs.length;
      content.push(loremParagraphs[idx]);
    }
    pages.push({
      id: `ch${chapterNum}-p${i + 1}`,
      number: i + 1,
      title: `Страница ${i + 1}`,
      content,
    });
  }
  return pages;
}

